// Generate a position trail leading backward from the vessel's current location.
// The trail array is stored newest-first (index 0 = most recent past point).
function generateTrail(
  lat: number,
  lng: number,
  cog: number,
  speed: number,
  count = 8,
): Position[] {
  const DEG    = Math.PI / 180;
  const nmToLat = 1 / 60;
  const nmToLng = (φ: number) => 1 / (60 * Math.cos(φ * DEG));

  const trail: Position[] = [];
  let curLat = lat;
  let curLng = lng;

  for (let i = 0; i < count; i++) {
    const minsBack = (i + 1) * 20;
    const distNm   = (speed * minsBack) / 60;
    const backBrg  = (cog + 180) % 360;
    curLat += distNm * nmToLat           * Math.cos(backBrg * DEG);
    curLng += distNm * nmToLng(curLat)   * Math.sin(backBrg * DEG);

    trail.push({
      id:                 `trail-${i}`,
      vessel_id:          "",
      mmsi:               "",
      latitude:           curLat + (Math.random() - 0.5) * 0.005,
      longitude:          curLng + (Math.random() - 0.5) * 0.010,
      speed_over_ground:  speed  + (Math.random() - 0.5) * 0.5,
      course_over_ground: (cog   + (Math.random() - 0.5) * 10 + 360) % 360,
      heading:            (cog   + (Math.random() - 0.5) *  5 + 360) % 360,
      nav_status:         "fishing",
      destination:        null,
      draught:            null,
      timestamp:          new Date(Date.now() - minsBack * 60 * 1000).toISOString(),
    });
  }
  return trail;
}

